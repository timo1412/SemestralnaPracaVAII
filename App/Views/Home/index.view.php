    <h4>Go Ju Ryu</h4>
    <p> Goju-ryu karate je všeobecne štýl, ktorý sa vyznačuje ako ťažké (zovretá päsť) a mäkké (otvorenou dlaňou alebo kruhové) techniky.
        Mnoho Goju-ryu praxe pocit, ako by sa bojové umenia techniky, v tom, že využívajú uhlov na odvrátenie štrajku skôr než sa snažiť splniť sile silou.
        Okrem toho, Goju-ryu inklinuje zdôrazniť stretnutie s oponentmi opak toho, čo ich využívajú.
        Napríklad sťahovanie hlavy (tvrdé časti tela) s otvorenou rukou (a mäkké časti tela) alebo úderom do slabín (soft) sa triesla kop (tvrdé).
        V našej organizácií sa cvičí tradičné okinawské Goju-ryu karate štýlu Shodokan, resp. Seigokan.
        Naši inštruktori sa pravidelne 2x ročne zúčastňujú tréningových pobytov na ostrove Okinawa.
        SSTKK pravidelne organizuje odborné semináre pre všetkých členov
    <h4>Kobudo</h4>
    <p> V SSTKK trénujeme tradičné okinawské kobudo štýlu Ryuei-ryu.
        V rámci školy sa cvičia nasledovné zbrane: kun (palica), sai, ekku (veslo), kama (kosák), timbe/teppi (krátky bodec/štít).
